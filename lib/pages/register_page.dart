class _RegisterPageState extends State<RegisterPage> {
  TextEditingController email = TextEditingController();
  TextEditingController password = TextEditingController();

  Future register() async {
    // var url = "http://192.168.1.101/flutter-login-signup/register.php";

    var url = Uri.parse("https://192.168.100.41/conexion_ejemplo/registro.php");
    var response = await http.patch(url, body: {
      "users_email": email.text,
      "users_password": password.text,
    });
    var data = json.decode(response.body);
    if (data == "Error") {
      Text('User allready exit!',
          style: TextStyle(fontSize: 25, color: Colors.red));
    } else {
      Text('Registration Successful',
          style: TextStyle(fontSize: 25, color: Colors.green));
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
        backgroundColor: Color(0xffF2F2F2),
        body: SafeArea(
          child: SingleChildScrollView(
            physics: BouncingScrollPhysics(),
            child: Container(
              height: MediaQuery.of(context).size.height * 0.9,
              child: Column(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: <Widget>[
                  Logo(titulo: 'Registro'),
                  _Form(),
                  Labels(
                    ruta: 'login',
                    titulo: '¿Ya tienes una cuenta?',
                    subTitulo: 'Ingresa ahora!',
                  ),
                  Text(
                    'Términos y condiciones de uso',
                    style: TextStyle(fontWeight: FontWeight.w200),
                  )
                ],
              ),
            ),
          ),
        ));
  }
}

